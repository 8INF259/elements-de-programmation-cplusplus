#include <iostream>
using namespace std;

int main() {
  // Ecriture d'une chaîne de caractères
cout << "bonjour!" << endl;

// Déclaration d'un entier, initialisé à 10
int i = 10;

// Déclaration d'un réel initialisé à 3.14159
double pi = 3.14159;

// Ecriture formattée d'un entier
cout << "i = " << i << endl;

// Ecriture formattée d'un double
cout << "pi = " << pi << endl;

  return 0;
}
